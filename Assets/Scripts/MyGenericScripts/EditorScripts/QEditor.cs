
using UnityEngine;

public class QEditor {

    public enum LineStyle { Solid, Dashed80, Dashed50, Dotted, DashedDotted }

    public static string GetLineStroke(LineStyle lineStyle)
    {
        var tempString = "";

        switch (lineStyle)
        {
            case LineStyle.Solid:
                tempString = ("————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————————");
                break;

            case LineStyle.Dashed80:
                tempString = ("––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––––");
                break;

            case LineStyle.Dashed50:
                tempString = ("------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
                break;

            case LineStyle.Dotted:
                tempString = ("······················································································································································································");
                break;
            case LineStyle.DashedDotted:
                tempString = ("·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·-·");
                break;
            default:
                tempString = ("······················································································································································································");
                break;
        }
        return tempString;
    }
}
